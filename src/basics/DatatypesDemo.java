package basics;

public class DatatypesDemo {
    public static void main(String[] args) {
        int x = 10; //x is my age

        /**
         * Integers -> byte, short, int, long
         * Decimal numbers -> float, double
         * characters -> char
         * sequence of characters(words, sentences, text) -> String
         * boolean -> true(1), false(0)
         */

        float y = 23.45F;
        double z = 1.23;

        boolean a = true; //1
        boolean b = false; //0

        char c = 'a';
        char d = '@';
        char e = ',';

        String name = "busyqa";
        String age = "12";

    }
}
